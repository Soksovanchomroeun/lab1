package kh.edu.rupp.demoapp.model;

public class TaskGroup {
    private int id;
    private String title;
    private int taskCount;
    private int percent;

    // Getters
    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public int getTaskCount() {
        return taskCount;
    }

    public int getPercent() {
        return percent;
    }
}
